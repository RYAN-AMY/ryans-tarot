function buildCards() {
  const cards = [];

  MAJOR_ARCANA.forEach((c) => {
    const id = `major-${String(c.number).padStart(2, "0")}`;
    const marseilleNum = String(c.number + 1).padStart(2, "0");
    const rwsNum = String(c.number).padStart(2, "0");
    cards.push({
      id,
      arcana: "major",
      suit: null,
      number: c.number,
      nameZh: c.nameZh,
      nameEn: c.nameEn,
      uprightMeaning: c.upright,
      reversedMeaning: c.reversed,
      keywords: c.keywords,
      imagery: c.imagery,
      symbols: c.symbols,
      lifeThemes: c.lifeThemes,
      imageRiderWaite: `m${rwsNum}.jpg`,
      imageMarseille: `a${marseilleNum}.jpg`,
    });
  });

  Object.entries(MINOR_ARCANA).forEach(([suit, suitCards]) => {
    const suitKey = suit.charAt(0);
    const marseilleSuitMap = { wands: "b", cups: "c", pentacles: "d", swords: "e" };
    const mKey = marseilleSuitMap[suit];

    suitCards.forEach((c) => {
      const num = String(c.number).padStart(2, "0");
      const id = `minor-${suit}-${num}`;
      cards.push({
        id,
        arcana: "minor",
        suit,
        number: c.number,
        nameZh: c.nameZh,
        nameEn: c.nameEn,
        uprightMeaning: c.upright,
        reversedMeaning: c.reversed,
        keywords: c.keywords,
        imagery: c.imagery,
        symbols: c.symbols,
        lifeThemes: c.lifeThemes,
        imageRiderWaite: `${suitKey}${num}.jpg`,
        imageMarseille: `${mKey}${num}.jpg`,
      });
    });
  });

  return cards;
}

export const allCards = buildCards();
export const majorArcana = allCards.filter((c) => c.arcana === "major");
export const minorArcana = allCards.filter((c) => c.arcana === "minor");
export const getCardById = (id) => allCards.find((c) => c.id === id);
